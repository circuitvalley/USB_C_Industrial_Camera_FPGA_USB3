                        .TP((hs_tx_en_i | lp_rx_en_i)), 
                        .TN((hs_tx_en_i | lp_rx_en_i)), 
                        .IHS(data_dif_w[i]), 
                        .HSRXEN(1'd0), 
                        .HSTXEN((hs_tx_en_i & (~lp_tx_en_i))), 
                        ///
                    .OHS(), 
                        .OLSP(lp_rx_data_p_w[i]), 
                        .OLSN(lp_rx_data_n_w[i]), 
                        ///
                    .BP(data_p_io[i]), 
                        .BN(data_n_io[i])) ; 
        end
    endgenerate
