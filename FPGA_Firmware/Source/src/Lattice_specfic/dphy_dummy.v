                .AN(lp_tx_clk_n_i), 
                .TP(hs_tx_clk_en_i), 
                .TN(hs_tx_clk_en_i), 
                .IHS(clk_dif_w), 
                .HSRXEN(1'd0), 
                .HSTXEN(hs_tx_clk_en_i), 
